import unittest
import torch
from torchvision.models.vgg import vgg16_bn as vgg16_bn_original

class TestVggFcn(unittest.TestCase):
    def test_vgg16(self):
        model = vgg16_bn_original(True)
        model_fcn = vgg16_bn(True)
        print(model_fcn)

        i,j = 1,1
        x = torch.randn(10,3,224*i,224*j)
        model.eval()
        model_fcn.eval()
        with torch.no_grad():
            y = model(x).view(-1)
            y_fcn = model_fcn(x).view(-1)

        diff = y - y_fcn
        self.assertTrue(torch.all(torch.abs(diff) < 2e-5))


if __name__ == '__main__':
    unittest.main()
