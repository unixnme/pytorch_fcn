import unittest
import torch
from torchvision.models import resnet50 as resnet50_original

class TestResNetFcn(unittest.TestCase):
    def test_resnet50(self):
        model = resnet50_original(pretrained=True)
        model_fcn = resnet50(pretrained=True)
        x = torch.randn(10,3,224,224)
        y = model(x).view(-1)
        y_fcn = model_fcn(x).view(-1)

        diff = y - y_fcn
        self.assertTrue(torch.all(torch.abs(diff) < 1e-5))


if __name__ == '__main__':
    unittest.main()
